	/**
	 * Tests the login method.
	 */
	@Test
	public void testLogin() {
		manager.clearData();
		manager.logout();
		
		// reference to empty directory from manager
		StudentDirectory dir = manager.getStudentDirectory();
		// check that it's empty
		assertTrue(dir.getStudentDirectory().length == 0);
		// add a student
		dir.addStudent("William", "Boyles", "wmboyle2", "wmboyle2@ncsu.edu", "password", "password", 18);
		// login using valid student credentials
		assertTrue(manager.login("wmboyle2", "password"));
		// login using invalid student credentials
		assertFalse(manager.login("wmboyle2", "notPassword"));

		manager.logout();
		
		// login using valid registrar credentials
		Properties p = new Properties();
		try (InputStream input = new FileInputStream("registrar.properties")) {
			p.load(input);
		} catch (IOException e) {
			throw new IllegalArgumentException("Cannot create regsitrar.");
		}
		assertTrue(manager.login(p.getProperty("id"), p.getProperty("pw")));
		manager.logout();
		// login using invalid registrar credentials for a bad id
		try {
			assertFalse(manager.login("badRegistrar", "badPassword"));
		} catch (IllegalArgumentException e) {
			assertEquals("User doesn't exist.", e.getMessage());
		}
		// login using invalid registrar credentials for a bad password
		try {
			assertFalse(manager.login(p.getProperty("id"), "badPassword"));
		} catch (IllegalArgumentException e) {
			assertEquals("Invalid id or password", e.getMessage());
		}

		manager.logout();
		
		// Trying to replicate Jenkins test
		manager.clearData();
		dir.addStudent("Emerald", "Frost", "efrost", "adipiscing@acipsumPhasellus.edu", "password", "password", 3);
		assertTrue(manager.login("efrost", "password"));
		assertFalse(manager.login("efrost", "password"));
		assertEquals(1, dir.getStudentDirectory().length);
		assertFalse(manager.login("efrost", "password"));
	}

